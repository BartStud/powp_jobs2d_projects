package edu.kis.powp.jobs2d.drivers.adapter.transformation;

public class TransformationPoint {

    private int x, y;

    public int getX() {
        return this.x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return this.y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public TransformationPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

}
